package epi;
import epi.test_framework.EpiTest;
import epi.test_framework.EpiTestComparator;
import epi.test_framework.LexicographicalListComparator;
import epi.test_framework.GenericTest;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.function.BiPredicate;
public class Permutations {
  @EpiTest(testDataFile = "permutations.tsv")

  public static List<List<Integer>> permutations(List<Integer> A) {
    // TODO - you fill in here.

    List<List<Integer>> ans = new ArrayList<>();

    sol(0 ,A, ans);


    return ans;
  }

  private static void sol( int i, List<Integer> ip, List<List<Integer>> ans) {
    if(i == ip.size()) {
      ans.add(new ArrayList<>(ip));
    }

    for ( int j = i; j< ip.size(); j++) {
      Collections.swap(ip, i, j);
      sol(i+1,ip,ans);
      Collections.swap(ip, i, j);
    }

  }

  @EpiTestComparator
  public static BiPredicate<List<List<Integer>>, List<List<Integer>>> comp =
      (expected, result) -> {
    if (result == null) {
      return false;
    }
    for (List<Integer> l : expected) {
      Collections.sort(l);
    }
    expected.sort(new LexicographicalListComparator<>());
    for (List<Integer> l : result) {
      Collections.sort(l);
    }
    result.sort(new LexicographicalListComparator<>());
    return expected.equals(result);
  };

  public static void main(String[] args) {
    System.exit(
        GenericTest
            .runFromAnnotations(args, "Permutations.java",
                                new Object() {}.getClass().getEnclosingClass())
            .ordinal());
  }
}
